public class positive_or_negative {
    public static void main(String[] args) {

        double num = -12.3  ;

        if (num<0.0)
        {
            System.out.println(num + "  Is negative number");
        }
        else if (num > 0.0)
        {
            System.out.println(num + "  Is positive number");
        }

        else
        {
            System.out.println(num + "  Number is 0 ");
        }

    }
}
